// Helper function to get formatted country list for dropdown
export function getCountryOptions() {
  const topCountries = TOP_RETIREMENT_ORIGIN_COUNTRIES;
  const otherCountries = ALL_COUNTRIES.filter(
    country => !topCountries.includes(country)
  );
  
  return {
    topCountries: topCountries.sort(),
    otherCountries: otherCountries.sort(),
    allCountries: ALL_COUNTRIES.sort()
  };
}

