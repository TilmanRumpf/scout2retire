// Function to get the best image for a town
export function getBestImageForTown(country, cityName) {
  const countryData = curatedCountryImages[country];
  
  if (!countryData) {
    console.warn(`No curated images for country: ${country}`);
    return null;
  }
  
  // Try to find specific city image
  if (countryData.cities && countryData.cities[cityName]) {
    return countryData.cities[cityName];
  }
  
  // Fall back to country default
  return countryData.default;
}