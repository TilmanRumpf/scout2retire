async function importAllData() {
  console.log('🔄 Importing government and political stability data for all 341 towns...\n')
  
  let successCount = 0
  let errorCount = 0
  
  for (const town of townData) {
    // Convert values to 0-100 scale if needed
    let govRating = town.gov
    let polRating = town.pol
    
    // If value is less than 10, it's likely already on 0-10 scale, so multiply by 10
    if (govRating < 10) govRating = govRating * 10
    if (polRating < 10) polRating = polRating * 10
    
    const { error } = await supabase
      .from('towns')
      .update({
        government_efficiency_rating: govRating,
        political_stability_rating: polRating
      })
      .eq('name', town.name)
      .eq('country', town.country)
    
    if (error) {
      console.error(`❌ ${town.name}, ${town.country}: ${error.message}`)
      errorCount++
    } else {
      console.log(`✅ ${town.name}, ${town.country}: gov=${govRating}, pol=${polRating}`)
      successCount++
    }
  }
  
  console.log(`\n📊 Import Complete:`)
  console.log(`✅ Success: ${successCount} towns`)
  console.log(`❌ Errors: ${errorCount} towns`)
  
  // Verify the import
  const { data: checkData, count } = await supabase
    .from('towns')
    .select('name', { count: 'exact' })
    .not('government_efficiency_rating', 'is', null)
  
  console.log(`\n🎯 Verification: ${count} towns now have government data`)
  console.log('💡 Admin scores should now show proper variation!')
  console.log('🔄 Clear browser cache to see updated scores')
}

